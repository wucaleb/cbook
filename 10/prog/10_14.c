/*利用指標常數來存取陣列的內容
-陣列與指標的等價性 (Array-Pointer Equivalence)
本程式證明：存取陣列元素時，使用「陣列索引」和「指標算術」是完全等效的。*/
#include<stdio.h>
int main(void)
{
    int a[3]={5,7,9};

    //a[0] (索引存取) vs *(a+0) (指標存取)
    //*(a+0) 表示「從 a 的起始位址開始，向後移動 0 個 int 單位，並取出內容」。
    printf("a[0]=%d, *(a+0)=%d\n", a[0], *(a+0));   
    printf("a[1]=%d, *(a+1)=%d\n", a[1], *(a+1));
    printf("a[2]=%d, *(a+2)=%d\n", a[2], *(a+2));

    return 0;
}

/*輸出結果：
a[0]=5, *(a+0)=5
a[1]=7, *(a+1)=7
a[2]=9, *(a+2)=9
-------------------------------
a[i] 的本質就是 *(a + i)
*/