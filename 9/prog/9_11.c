/*三維陣列與初值的設定*/
#include<stdio.h>
int main(void)
{
    int A[2][4][3] = {{{21, 32, 34},
                       {33, 66, 55},
                       {31, 45, 67},
                       {35, 35, 89}},
                      {{11, 77, 78},
                       {90, 13, 76},
                       {56, 789, 24},
                       {78, 63, 14}}};
    int i,j,k,max=A[0][0][0];
    
    for(i = 0;i < 2;i++)
        for(j = 0;j < 4;j++)
            for(k = 0;k < 3;k++)
            if(max< A[i][j][k])
                max=A[i][j][k];
                
printf("max=%d\n",max);
return 0;
    
}

/*
【程式碼邏輯總結】
1. 最大值初始化：將 max 設為第一個元素 A[0][0][0] (21)。
2. 遍歷比較：使用三層迴圈確保程式訪問到陣列中的所有 2 * 4 * 3 = 24 個元素。
3. 更新機制：每當 A[i][j][k] 大於當前的 max 值時，就將 max 更新為這個新的更大值。
*/